package fi.vm.kapa.rova.ptv.model;

public class PtvSearchResult {

    private PtvService _source;
    private String _id;
    private String _index;
    private int _score;

    public PtvService get_source() {
        return _source;
    }

    public void set_source(PtvService _source) {
        this._source = _source;
    }

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String get_index() {
        return _index;
    }

    public void set_index(String _index) {
        this._index = _index;
    }

    public int get_score() {
        return _score;
    }

    public void set_score(int _score) {
        this._score = _score;
    }
}
