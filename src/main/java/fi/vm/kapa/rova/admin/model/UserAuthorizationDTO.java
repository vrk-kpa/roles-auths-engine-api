package fi.vm.kapa.rova.admin.model;


public class UserAuthorizationDTO {

    private long id;
    private UserDTO user;
    private String roleName;
    private String serviceUuid;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public UserDTO getUser() {
        return user;
    }

    public void setUser(UserDTO user) {
        this.user = user;
    }
    
    public String getRoleName() {
        return roleName;
    }

    public void setRoleName(String roleName) {
        this.roleName = roleName;
    }

    public String getServiceUuid() {
        return serviceUuid;
    }

    public void setServiceUuid(String serviceUuid) {
        this.serviceUuid = serviceUuid;
    }

    @Override
    public String toString() {
        return "UserAuthorizationDTO [roleName=" + roleName + ", serviceUuid="
                + serviceUuid + "]";
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result
                + ((roleName == null) ? 0 : roleName.hashCode());
        result = prime * result
                + ((serviceUuid == null) ? 0 : serviceUuid.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        UserAuthorizationDTO other = (UserAuthorizationDTO) obj;
        if (roleName == null) {
            if (other.roleName != null)
                return false;
        } else if (!roleName.equals(other.roleName))
            return false;
        if (serviceUuid == null) {
            if (other.serviceUuid != null)
                return false;
        } else if (!serviceUuid.equals(other.serviceUuid))
            return false;
        return true;
    }
}
