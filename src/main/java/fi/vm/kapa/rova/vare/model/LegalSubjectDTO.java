package fi.vm.kapa.rova.vare.model;

import java.util.Set;

public class LegalSubjectDTO {

    private String id;
    private String firstNames;
    private String lastName;
    private boolean company;
    private String callingName;

    public String getCallingName() {
        return callingName;
    }

    public void setCallingName(String callingName) {
        this.callingName = callingName;
    }

    public Set<String> getChecksums() {
        return checksums;
    }

    public void setChecksums(Set<String> checksums) {
        this.checksums = checksums;
    }

    private String companyName;
    private boolean valid;

    private Set<String> checksums;
    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFirstNames() {
        return firstNames;
    }

    public void setFirstNames(String firstNames) {
        this.firstNames = firstNames;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public boolean isCompany() {
        return company;
    }

    public void setCompany(boolean company) {
        this.company = company;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public boolean isValid() {
        return valid;
    }

    public void setValid(boolean valid) {
        this.valid = valid;
    }

    @Override
    public String toString() {
        return "LegalSubjectDTO [id=" + id + ", firstNames=" + firstNames
                + ", lastName=" + lastName + ", company=" + company
                + ", companyName=" + companyName + ", valid=" + valid + "]";
    }
}
